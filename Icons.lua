function Icons:Get(name)
    return ids[name] or ""
end

function Icons:Label(parent, name, size, color)
    local img = Instance.new("ImageLabel")
    img.Size = UDim2.new(0, size or 18, 0, size or 18)
    img.BackgroundTransparency = 1
    img.Image = ids[name] or ""
    img.ImageColor3 = color or Color3.fromRGB(255, 255, 255)
    img.ScaleType = Enum.ScaleType.Fit
    img.Parent = parent
    return img
end

function Icons:Button(parent, name, size, color)
    local img = Instance.new("ImageButton")
    img.Size = UDim2.new(0, size or 18, 0, size or 18)
    img.BackgroundTransparency = 1
    img.Image = ids[name] or ""
    img.ImageColor3 = color or Color3.fromRGB(255, 255, 255)
    img.ScaleType = Enum.ScaleType.Fit
    img.AutoButtonColor = false
    img.Parent = parent
    return img
end

function Icons:List()
    local out = {}
    for k in pairs(ids) do table.insert(out, k) end
    table.sort(out)
    return out
end

function Icons:AddCustom(name, assetId)
    ids[name] = assetId
end

return Icons
